/*
 final
 	关键字，修饰符
 	
 	作用：
 		1. 修饰类
 			写在public等后面，class前面。
 			
 			final修饰的类无法被继承。
 			
 		2. 修饰方法
 		
 			不能重写。
 		
 		3. 修饰变量
 				1. 局部变量
 					final修饰的局部变量的值一旦确立，不可更改
 				
 				2. 成员变量
 					（属性）
 					一般用final和static一起修饰成员变量，成员变量就成了一个常量。
 					此时是一个常量，必须赋值。
 					而且常量名要大写。
  
  
 **/
