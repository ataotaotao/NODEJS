/*
 * 
 * 
 	访问修饰符
 		java中有很多的修饰符
 		
 		修饰访问权限符用于控制访问权限	
 			public
 				都可以访问
 			private
 				只有自己可以访问
 			protected
 				类以及子类还有同包下，可以访问到。
 			default(默认)
  				同包下可以访问
  		1. 在自己类中，无论是哪个控制权限修饰符修饰的，都可以访问
  		2. 同一个包下的子类，只有private修饰的无法访问。
  		3. 同一个包下的类，private还是不能访问。
  		4. 不同包下的子类，只有public的才能访问。
  		5. 不同包下的类，只有public才行。
  
  
 */
